elem[19] = document.getElementById("totals"); 


var scoreTotal = 0; 
var totalPar = 72; 
var totalOver = 0; 

// create add1 function
function add1(elem, elem19) {
    if (elem.children[2].innerHTML == "-") { 
        elem.children[2].innerHTML = "0"; 

        elem19.children[2].inneHTML = scoreTotal; 
    }
    if (elem19.children[2].innerHTML == "-") { 
        elem19.children[2].innerHTML = "1";
    }


    elem19.children[1].innerHTML = totalPar; 


    
    let currentScore = elem.children[2].innerHTML;
    currentScore = Number.parseInt(currentScore); 
    elem.children[2].innerHTML = currentScore + 1; 
    scoreTotal = scoreTotal + 1;
    elem19.children[2].innerHTML = scoreTotal; 


    //over calculation
    if (elem.children[2].innerHTML == 5) { 
        elem.children[3].innerHTML = "0"; 
        elem19.children[2].inneHTML = totalOver; 
    }
    if (elem19.children[3].innerHTML == "-" && elem.children[2].innerHTML == 5) { 
        elem19.children[3].innerHTML = 1; 

    }
    if (elem.children[2].innerHTML > 4) { 
        currentover = elem.children[2].innerHTML - elem.children[1].innerHTML; 
        elem.children[3].innerHTML = currentover;
        totalOver = totalOver + 1; 
        elem19.children[3].innerHTML = totalOver;
    }
}





//score calculations
function subt1(elem, elem19) { 
    if (elem.children[2].innerHTML > "0") { 
        let currentScore = elem.children[2].innerHTML; 
        currentScore = Number.parseInt(currentScore); 
        elem.children[2].innerHTML = currentScore - 1;
        scoreTotal = scoreTotal - 1; 
        elem19.children[2].innerHTML = scoreTotal;
    }

    //over calculations
    if (elem.children[2].innerHTML >= 4) { 
        let currentover; 
        currentover = elem.children[2].innerHTML - elem.children[1].innerHTML; 
        totalOver = totalOver - 1; 
        elem.children[3].innerHTML = currentover;
        elem19.children[3].innerHTML = totalOver;
    }
}


//clearing rows using C button
function clearrow(elem, elem19) { 
    let curscore = elem.children[2].innerHTML;
    let curover = elem.children[3].innerHTML;
    if (curscore != "-") { 
        elem19.children[2].innerHTML = scoreTotal - curscore; 
        scoreTotal = scoreTotal - curscore; 
    }
    if (curover != "-") { 
        elem19.children[3].innerHTML = totalOver - curover; 
        totalOver = totalOver - curover; 
    }
    elem.children[2].innerHTML = "-"; 
    elem.children[3].innerHTML = "-"; 
}

